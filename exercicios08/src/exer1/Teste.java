package exer1;

public class Teste {
  public static void main(String[] args) {
    int valor1 = 12;
    byte valor2 = (byte) valor1;

    System.out.println("Valor 1(Int): " + valor1 +
        "\nValor 2(Byte): " + valor2);

    double valor3 = 1235.56;
    float valor4 = (float) valor3;

    System.out.println("Valor 3(Double): " + valor3 +
        "\nValor 4(Float): " + valor4);
  }

}
