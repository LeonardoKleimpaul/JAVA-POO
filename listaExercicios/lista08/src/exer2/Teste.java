public class Teste {
  public static void main(String[] args) {

    double numero1 = 1234.56;
    float numero2 = (float) numero1;

    int numero3 = 12;
    byte numero4 = (byte) numero3;

    System.out.println("Número 1(Double): " + numero1 +
        "\nNúmero 2(Float): " + numero2 +
        "\nNúmero 3(Int): " + numero3 +
        "\nNúmero 4(Byte): " + numero4);
  }

}
