package ativ2;

public class Televisao {

  private int canal = 5;
  private int volume = 20;
  private int canalAux;

  public int getCanal() {
    return canal;
  }

  public void setCanal(int canal) {
    this.canal = canal;
  }

  public int getVolume() {
    return volume;
  }

  public void setVolume(int volume) {
    this.volume = volume;
  }

  public int getCanalAux() {
    return canalAux;
  }

  public void setCanalAux(int canalAux) {
    this.canalAux = canalAux;
  }

}
